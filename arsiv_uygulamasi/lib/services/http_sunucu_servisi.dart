            '‚úÖ Metadata alƒ±ndƒ±: ${metadata.substring(0, metadata.length.clamp(0, 100))}...',
          );
        } else if (contentDisposition.contains('name="file"')) {
          fileBytes = data;

          // Filename'i header'dan √ßƒ±kar
          final filenameMatch = RegExp(
            r'filename="([^"]*)"',
          ).firstMatch(contentDisposition);
