  Future<String> _handleConnect(HttpRequest request) async {
    try {
      print('üîó Yeni baƒülantƒ± isteƒüi alƒ±ndƒ±');

      final bodyBytes = await request.fold<List<int>>(
        <int>[],
        (previous, element) => previous..addAll(element),
      );
      final body = utf8.decode(bodyBytes);
      final data = json.decode(body);

      final clientId = data['clientId'] as String?;
      final clientName = data['clientName'] as String?;
      final clientIP =
          request.connectionInfo?.remoteAddress?.address ?? 'bilinmiyor';

      if (clientId == null || clientName == null) {
        return json.encode({'error': 'clientId ve clientName gerekli'});
      }

      // Basit token olu≈ütur
      final token = 'token_${DateTime.now().millisecondsSinceEpoch}';

      // Server'ƒ±n kendi IP'sini al (local network IP)
      String? serverIP;
      try {
        // Local network interface'ini bul
        final interfaces = await NetworkInterface.list();
        for (final interface in interfaces) {
          if (interface.name.toLowerCase().contains('wi-fi') ||
              interface.name.toLowerCase().contains('wlan') ||
              interface.name.toLowerCase().contains('ethernet')) {
            for (final addr in interface.addresses) {
              if (addr.type == InternetAddressType.IPv4 &&
                  !addr.isLoopback &&
                  addr.address.startsWith('192.168.')) {
                serverIP = addr.address;
                break;
              }
            }
            if (serverIP != null) break;
          }
        }
        // Fallback: any valid local IP
        if (serverIP == null) {
          for (final interface in interfaces) {
            for (final addr in interface.addresses) {
              if (addr.type == InternetAddressType.IPv4 &&
                  !addr.isLoopback &&
                  (addr.address.startsWith('192.168.') ||
                      addr.address.startsWith('10.') ||
                      addr.address.startsWith('172.'))) {
                serverIP = addr.address;
                break;
              }
            }
            if (serverIP != null) break;
          }
        }
      } catch (e) {
        print('‚ö†Ô∏è Server IP alƒ±namadƒ±: $e');
        serverIP = 'localhost';
      }

      // Baƒülantƒ± ba≈üarƒ±lƒ± bildirimi
      print('üéâ BAƒûLANTI BA≈ûARILI! Mobil cihaz baƒülandƒ±');
      print('üì± Baƒülanan cihaz: $clientName ($clientId)');
      print('üì± Client IP: $clientIP');
      print('üíª Server IP: $serverIP');

      // UI'ya bildirim g√∂nder - HEMEN
      final deviceInfo = {
        'clientId': clientId,
        'clientName': clientName,
        'ip': clientIP,
        'timestamp': DateTime.now().toIso8601String(),
        'platform': data['platform'] ?? 'Mobil',
        'belgeSayisi': data['belgeSayisi'] ?? 0,
        'toplamBoyut': data['toplamBoyut'] ?? 0,
      };

      // Callback'i √ßaƒüƒ±r
      if (_onDeviceConnected != null) {
        print('üì¢ Callback √ßaƒürƒ±lƒ±yor...');
        Future.microtask(() => _onDeviceConnected!(deviceInfo));
      } else {
        print('‚ö†Ô∏è Callback tanƒ±mlanmamƒ±≈ü!');
      }

      return json.encode({
        'success': true,
        'token': token,
        'serverId': _cihazId,
        'serverName': _cihazAdi,
        'serverIP': serverIP, // ‚úÖ EKLENEN: Server IP bilgisi
        'serverPort': SUNUCU_PORTU, // ‚úÖ EKLENEN: Server port bilgisi
        'message': 'Baƒülantƒ± kuruldu',
        'serverInfo': {
          'platform': _platform,
          'belgeSayisi': await _veriTabani.toplamBelgeSayisi(),
          'toplamBoyut': await _veriTabani.toplamDosyaBoyutu(),
          'ip': serverIP, // ‚úÖ EKLENEN: Duplicate ama uyumluluk i√ßin
        },
        // ‚úÖ EKLENEN: Bidirectional sync i√ßin endpoint bilgileri
        'endpoints': {
          'upload': 'http://$serverIP:$SUNUCU_PORTU/upload',
          'download': 'http://$serverIP:$SUNUCU_PORTU/download',
          'documents': 'http://$serverIP:$SUNUCU_PORTU/documents',
          'connect': 'http://$serverIP:$SUNUCU_PORTU/connect',
        },
      });
    } catch (e) {
      print('‚ùå Connect handler hatasƒ±: $e');
      return json.encode({'error': 'Baƒülantƒ± hatasƒ±', 'message': e.toString()});
    }
  }

