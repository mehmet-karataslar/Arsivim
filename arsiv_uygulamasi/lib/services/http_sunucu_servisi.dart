  // Belge indirme endpoint'i - Hash ve dosya adƒ± destekli
  Future<String> _handleDownload(HttpRequest request) async {
    try {
      // URL'den hash veya dosya adƒ±nƒ± al
      String requestParam;
      try {
        requestParam = Uri.decodeComponent(request.uri.pathSegments.last);
      } catch (e) {
        requestParam = request.uri.pathSegments.last;
        print('‚ö†Ô∏è URL decode hatasƒ±, raw string kullanƒ±lƒ±yor: $e');
      }
      print('üì• Belge indirme isteƒüi: $requestParam');

      BelgeModeli? belge;

      // √ñnce hash ile ara
      belge = await _veriTabani.belgeGetirByHash(requestParam);

      if (belge == null) {
        print('üìã Hash ile bulunamadƒ±, dosya adƒ± ile aranƒ±yor...');
        // Dosya adƒ± ile ara
        List<BelgeModeli> belgeler = await _veriTabani.belgeAra(requestParam);

        if (belgeler.isEmpty) {
          print('üìã ƒ∞lk arama sonu√ßsuz, farklƒ± encode t√ºrleri deneniyor...');

          // Farklƒ± encode varyasyonlarƒ±nƒ± dene
          final aramaTerimleri = [
            requestParam,
            Uri.encodeComponent(requestParam),
            requestParam.replaceAll('%20', ' '),
            requestParam.replaceAll('+', ' '),
          ];

          for (final terim in aramaTerimleri) {
            belgeler = await _veriTabani.belgeAra(terim);
            if (belgeler.isNotEmpty) {
              print('‚úÖ Belge bulundu: $terim');
              break;
            }
          }
        }

        if (belgeler.isEmpty) {
          print('‚ùå Belge hi√ßbir t√ºrde bulunamadƒ±: $requestParam');
          request.response.statusCode = 404;
          await request.response.close();
          return json.encode({'error': 'Belge bulunamadƒ±'});
        }

        belge = belgeler.first;
      }

      final dosya = File(belge.dosyaYolu);
      if (!await dosya.exists()) {
        print('‚ùå Dosya fiziksel olarak bulunamadƒ±: ${belge.dosyaYolu}');
        request.response.statusCode = 404;
        await request.response.close();
        return json.encode({'error': 'Dosya bulunamadƒ±'});
      }

      final dosyaBytes = await dosya.readAsBytes();
      final safeDosyaAdi = belge.dosyaAdi.replaceAll(
        RegExp(r'[^\w\-_\.]'),
        '_',
      );

      request.response
        ..headers.contentType = ContentType.binary
        ..headers.contentLength = dosyaBytes.length
        ..headers.add(
          'Content-Disposition',
          'attachment; filename=$safeDosyaAdi',
        )
        ..headers.add('Access-Control-Allow-Origin', '*')
        ..headers.add('Access-Control-Expose-Headers', 'Content-Disposition');

      request.response.add(dosyaBytes);
      await request.response.close();

      print(
        '‚úÖ Belge g√∂nderildi: ${belge.dosyaAdi} (${dosyaBytes.length} bytes)',
      );
      return 'BINARY_SENT';
    } catch (e) {
      print('‚ùå Download endpoint hatasƒ±: $e');
      try {
        request.response.statusCode = 500;
        await request.response.close();
      } catch (closeError) {
        print('‚ö†Ô∏è Response kapatma hatasƒ±: $closeError');
      }
      return json.encode({'error': 'ƒ∞ndirme hatasƒ±: $e'});
    }
  }

